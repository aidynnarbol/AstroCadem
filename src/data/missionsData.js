export const getAllMissions = () => {
  const allMissions = [];
  for (const planet of Object.values(planetsData)) {
    planet.missionsList.forEach(mission => {
      allMissions.push({
        ...mission,
        planetId: planet.id,
        planetName: planet.name,
        planetEmoji: planet.emoji
      });
    });
  }
  return allMissions;
};

